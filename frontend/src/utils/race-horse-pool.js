// ランダムな馬名生成（L以下のフィクション馬用）
const NPC_PREFIXES = ['サクラ','ダイワ','メイショウ','マイネル','エアー','タイキ','ゴールド','シルバー','ブラック','ホワイト','ミラクル','サンライズ','ムーン','スター','ロード'];
const NPC_SUFFIXES = ['スター','フラッシュ','ドリーム','ウィング','ブレイブ','ハート','ソウル','スピリット','ライト','ストーム','ブレイズ','ホープ','グロリア','ビクトリー','シャイン'];

function generateNPCName() {
  const p = NPC_PREFIXES[Math.floor(Math.random() * NPC_PREFIXES.length)];
  const s = NPC_SUFFIXES[Math.floor(Math.random() * NPC_SUFFIXES.length)];
  return p + s;
}

// グレードに応じてNPC馬を生成する関数
// G3以上はrace-horse-poolの実名馬から抽選
// L以下はランダム名前＋ステータス自動生成
export function generateNPCHorses(raceGrade, raceName, count = 7) {
  const pool = getHorsePoolForRace(raceName);
  const RUNNING_STYLES = ['逃げ', '先行', '差し', '追込'];

  if (pool && ['g3', 'g2', 'g1'].includes(raceGrade)) {
    // 実名馬プールからランダムにcount頭選ぶ
    const shuffled = [...pool].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count).map(h => ({
      name: h.name,
      speed: h.speed,
      stamina: h.stamina,
      stability: h.stability,
      burst: h.burst,
      runningStyle: RUNNING_STYLES[Math.floor(Math.random() * RUNNING_STYLES.length)],
      isNPC: true,
    }));
  }

  // L以下：名前・ステータスをランダム生成
  const table = GRADE_STAT_TABLES[raceGrade] ?? GRADE_STAT_TABLES['open'];
  function rollStat() {
    const r = Math.random() * 100;
    let cum = 0;
    for (const [rank, pct] of table) {
      cum += pct;
      if (r < cum) return rank;
    }
    return table[table.length - 1][0];
  }

  return Array.from({ length: count }, () => ({
    name: generateNPCName(),
    speed: rollStat(),
    stamina: rollStat(),
    stability: rollStat(),
    burst: rollStat(),
    runningStyle: RUNNING_STYLES[Math.floor(Math.random() * RUNNING_STYLES.length)],
    isNPC: true,
  }));
}
